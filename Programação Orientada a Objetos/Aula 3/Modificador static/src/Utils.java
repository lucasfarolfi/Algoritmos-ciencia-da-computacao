public class Utils {

    public static void CalculaMedia_2(double n1, double n2){
        double media = 0;
        media = (n1 + n2)/2;
        
        System.out.println("Media: " + media);
    }

    public static void CalculaMedia_3(double n1, double n2, double n3){
        double media = 0;
        media = (n1 + n2 + n3)/3;
        
        System.out.println("Media: " + media);
    }

    public static void CalculaMedia_4(double n1, double n2, double n3, double n4){
        double media = 0;
        media = (n1 + n2 + n3 + n4)/4;
        
        System.out.println("Media: " + media);
    }
}
